package day2;

public class Ex01Var {

	public static void main(String[] args) {
		
		//변수의 선언(Declare)
		int box; //정수형 변수 box를 사용할거야
		
		//System.out.println(box);
		
		//변수의 할당(Assignment)
		box = 3;   //최초 할당 ->초기
		box = 100; //재할
		int box2 = 10; //선언과 동시에 초기
		System.out.println(box2);
		
	}

}