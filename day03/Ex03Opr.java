package day3;

public class Ex03Opr {
	public static void main(String[] args) {
		
		
		// 4 비교 연산자
		// 두 개의 값을 비교 -> 결과를 boolean 자료형으로 리턴
		
		System.out.println(2 < 4);
		System.out.println(3 < 3);
		System.out.println(3 <= 3);
		System.out.println(10 > 20);
		
		System.out.println(10 == 10);
		System.out.println(10 != 10);
		
		// 비교 연산자의 결과: boolean
	}
}
