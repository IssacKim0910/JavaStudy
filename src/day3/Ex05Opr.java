package day3;

public class Ex05Opr {
    public static void main(String[] args) {

        System.out.println(3 > 2 && 10 < 20);
        System.out.println(3 > 2 && 10 > 20);
        System.out.println(3 < 2 && 10 < 20);
        System.out.println(3 < 2 && 10 > 20);

        System.out.println(3 > 2 || 10 < 20);
        System.out.println(3 > 2 || 10 > 20);
        System.out.println(3 < 2 || 10 < 20);
        System.out.println(3 < 2 || 10 > 20);




    }
}
