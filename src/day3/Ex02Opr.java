package day3;

public class Ex02Opr {
    public static void main(String[] args) {

        //3. 증감연산자

        int num = 10;
        num++;
        //먼저 num을 호출된 이후에 1을 더한다

        System.out.println(num);

        num--;
        System.out.println(num);

        ++num;
        System.out.println(num);

        --num;
        System.out.println(num);
    }


}
